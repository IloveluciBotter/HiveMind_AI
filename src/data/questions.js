export function getQuestionsForLevel(level, count = 10) {
  const maxLevel = Math.min(level, 5)
  const eligibleQuestions = questionBank.filter(q => q.level <= maxLevel)
  const shuffled = [...eligibleQuestions].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getCategoriesFromQuestions(questions) {
  const categories = new Set()
  questions.forEach(q => categories.add(q.category))
  return Array.from(categories)
}
